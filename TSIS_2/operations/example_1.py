zero, two, five = 0, 2, 5

print("line #1:", not(zero))        # not(0) = True
print("line #2:", two or five)      # 2 or 5 = 2
print("line #3:", five ** two)      # 5^2 = 25
print("line #4:", zero ** zero)     # 0^0 = 1
print("line #5:", five:= 10)        # five = 10
five -= 5                       # five = 10 - 5 = 5
print("line #6:", five % two and two ** two)  # 5 % 2 and 2^2 = 4
